	$rotem_navigator_settings_options = get_option( 'rotem_navigator_settings_option_name' );



	echo '
	<script>
		let rotemHeaderTitle = "Jump to:";
		let rotemWrappingClass = 0;
	</script>
	';

	if (isset($rotem_navigator_settings_options['navigator_s_header_title_0']) && !empty($rotem_navigator_settings_options['navigator_s_header_title_0'])) {
		echo '
		<script>
			rotemHeaderTitle = "'.htmlentities($rotem_navigator_settings_options['navigator_s_header_title_0']).'";
		</script>
	';
	}
	

