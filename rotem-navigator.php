class RotemNavigatorSettings {
	private $rotem_navigator_settings_options;

	public function __construct() {
		add_action( 'admin_menu', array( $this, 'rotem_navigator_settings_add_plugin_page' ) );
		add_action( 'admin_init', array( $this, 'rotem_navigator_settings_page_init' ) );
	}

	public function rotem_navigator_settings_add_plugin_page() {
		add_options_page(
			'Rotem Navigator Settings', // page_title
			'Rotem Navigator Settings', // menu_title
			'manage_options', // capability
			'rotem-navigator-settings', // menu_slug
			array( $this, 'rotem_navigator_settings_create_admin_page' ) // function
		);
	}

	public function rotem_navigator_settings_create_admin_page() {
		$this->rotem_navigator_settings_options = get_option( 'rotem_navigator_settings_option_name' ); ?>

		<div class="wrap">
			<h2>Rotem Navigator</h2>
			<p>You can leave the fields empty and the plugin will use default values.</p>
			<?php settings_errors(); ?>

			<form method="post" action="options.php">
				<?php
					settings_fields( 'rotem_navigator_settings_option_group' );
					do_settings_sections( 'rotem-navigator-settings-admin' );
					submit_button();
				?>
			</form>
		</div>
	<?php }

	public function rotem_navigator_settings_page_init() {
		register_setting(
			'rotem_navigator_settings_option_group', // option_group
			'rotem_navigator_settings_option_name', // option_name
			array( $this, 'rotem_navigator_settings_sanitize' ) // sanitize_callback
		);

		add_settings_section(
			'rotem_navigator_settings_setting_section', // id
			'Settings', // title
			array( $this, 'rotem_navigator_settings_section_info' ), // callback
			'rotem-navigator-settings-admin' // page
		);

		add_settings_field(
			'navigator_s_header_title_0', // id
			'Navigator\'s header title', // title
			array( $this, 'navigator_s_header_title_0_callback' ), // callback
			'rotem-navigator-settings-admin', // page
			'rotem_navigator_settings_setting_section' // section
		);

	}

	public function rotem_navigator_settings_sanitize($input) {
		$sanitary_values = array();
		if ( isset( $input['navigator_s_header_title_0'] ) ) {
			$sanitary_values['navigator_s_header_title_0'] = sanitize_text_field( $input['navigator_s_header_title_0'] );
		}


		return $sanitary_values;
	}

	public function rotem_navigator_settings_section_info() {
		
	}

	public function navigator_s_header_title_0_callback() {
		printf(
			'<input class="regular-text" type="text" name="rotem_navigator_settings_option_name[navigator_s_header_title_0]" id="navigator_s_header_title_0" value="%s">',
			isset( $this->rotem_navigator_settings_options['navigator_s_header_title_0'] ) ? esc_attr( $this->rotem_navigator_settings_options['navigator_s_header_title_0']) : ''
		);
	}

}
